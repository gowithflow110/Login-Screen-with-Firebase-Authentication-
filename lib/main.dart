import 'package:flutter/material.dart';
import 'package:firebase_core/firebase_core.dart';
import 'package:firebase_auth/firebase_auth.dart';
import 'package:google_fonts/google_fonts.dart';

import 'firebase_options.dart'; // 🔧 Auto-generated by `flutterfire configure`
import 'screens/sign_in_screen.dart';
import 'screens/cv_form_screen.dart';
import 'screens/splash_login_screen.dart';

void main() async {
  WidgetsFlutterBinding.ensureInitialized();
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );

  runApp(const CVGeneratorApp());
}

class CVGeneratorApp extends StatelessWidget {
  const CVGeneratorApp({super.key});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'CVGenerator',
      debugShowCheckedModeBanner: false,
      theme: ThemeData(
        useMaterial3: true,
        colorScheme: ColorScheme.fromSeed(seedColor: Colors.blue),
        textTheme: GoogleFonts.poppinsTextTheme(), // ✅ Poppins globally
      ),
      initialRoute: '/', // ✅ Recommended for clarity
      routes: {
        '/': (context) => const AuthGate(), // Acts as home screen router
        '/sign_in': (context) => const SignInScreen(),
        '/cv_form': (context) => const CVFormScreen(),
      },
    );
  }
}

/// ✅ AuthGate: Decides where to navigate based on login status
class AuthGate extends StatelessWidget {
  const AuthGate({super.key});

  @override
  Widget build(BuildContext context) {
    return StreamBuilder<User?>(
      stream: FirebaseAuth.instance.authStateChanges(),
      builder: (context, snapshot) {
        // 🔄 Show splash screen while waiting
        if (snapshot.connectionState == ConnectionState.waiting) {
          return const SplashLoginScreen();
        }

        // ✅ If user is logged in, go to CV Form screen
        if (snapshot.hasData && snapshot.data != null) {
          return const CVFormScreen();
        }

        // ❌ Not logged in, show sign-in screen
        return const SignInScreen();
      },
    );
  }
}
