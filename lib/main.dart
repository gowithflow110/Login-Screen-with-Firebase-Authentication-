  runApp(const CVGeneratorApp());
}

class CVGeneratorApp extends StatelessWidget {
  const CVGeneratorApp({super.key});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'CVGenerator',
      debugShowCheckedModeBanner: false,
      theme: ThemeData(
        useMaterial3: true,
        colorScheme: ColorScheme.fromSeed(seedColor: Colors.blue),
        textTheme: GoogleFonts.poppinsTextTheme(), // âœ… Poppins globally
      ),
      home: const AuthGate(), // ğŸ‘ˆ Auth gate handles routing
    );
  }
}

// âœ… Acts as the logic gate between splash/login/dashboard
class AuthGate extends StatelessWidget {
  const AuthGate({super.key});

  @override
  Widget build(BuildContext context) {
    // ğŸ” DEBUG: Check current user status on each build
    User? user = FirebaseAuth.instance.currentUser;
    if (user != null) {
      print('âœ… User is logged in: ${user.email}');
    } else {
      print('âŒ No user is logged in');
    }

    return StreamBuilder<User?>(
      stream: FirebaseAuth.instance.authStateChanges(),
      builder: (context, snapshot) {
        // â³ Still waiting for auth state
        if (snapshot.connectionState == ConnectionState.waiting) {
          return const SplashLoginScreen();
        }

        // âœ… User is logged in
        if (snapshot.hasData && snapshot.data != null) {
          return const CVFormScreen();
        }

        // âŒ User not logged in
        return const SignInScreen();
      },
    );
  }
}
